#mengkonversi tipe data (casting)

""""
int() - membuat bilangan bulat dari literal bilangan bulat, 
literal bilangan pecahan (dengan menghilangkan semua desimal), 
atau literal string (dengan syarat string tersebut
 merupakan bilangan bulat)
 """

x = int(2)   # x akan jadi 2
y = int(5.8) # y akan jadi 5
z = int("310") # z akan jadi 310

#float()
x = float(25)     # x jadi 25.0
y = float(2.8)   # y jadi 2.8
z = float("3")   # z jadi 3.0

